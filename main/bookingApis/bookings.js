let bookingIds = [];
function getBookings() {
  let request = bookingsApi.url + `${bookingsApi.path.getBookings}`;
  let response = http.get(request);
  check(response, {
    "Get Bookings Response code is 200": (response) => response.status === 200,
  });
}

function createBookings() {
  const headers = {
    "Content-Type": "application/json",
    Accept: "application/json",
  };
  let request = bookingsApi.url + bookingsApi.path.createBooking;
  let numberOfBookings = bookings.length;
  for (let i = 0; i < numberOfBookings; i++) {
    let data = JSON.stringify({
      firstname: bookings[i].firstname,
      lastname: bookings[i].lastname,
      totalprice: bookings[i].totalprice,
      depositpaid: bookings[i].depositpaid,
      bookingdates: {
        checkin: bookings[i].checkin,
        checkout: bookings[i].checkout,
      },
      additionalneed: bookings[i].additionalneeds,
    });
    let response = http.post(request, data, { headers: headers });
    bookingIds.push(JSON.parse(response.body).bookingid);
    check(response, {
      "Create Bookings Response code is 200": (response) =>
        response.status === 200,
    });
  }
}

